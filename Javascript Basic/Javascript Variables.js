//Javascript Variables
// Variables are containers for storing data values
// In JavaScript, there are three ways to declare variables: var, let, and const

// var - var is used to declare variables that can be reassigned

var Myname = "lalith mohan";

// let - let isused to declare variables that can be reassigned

let age = 25;

// const - const is used to declare variables that cannot be reassigned

const DOB = 2000;

// Displaying the output in the console

console.log("Name:", Myname);
console.log("Age:", age);
console.log("Date of Birth:", DOB);
